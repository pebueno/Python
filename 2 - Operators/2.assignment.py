# EQUAL
a = 1
b = 10
c = 15
d = 20
e = 50

# SUM
a += 5  # Equivalent of: a = a + 5

# MINUS
b -= 1  # Equivalent of: b = b - 1

# PLUS
c *= 1  # Equivalent of: c = c * 1

# SPLIT
d //= 2  # Equivalent of: d = d / 2

# REST
e %= 1  # Equivalent of: x = e % 1

print(a)
print(b)
print(c)
print(d)
print(e)